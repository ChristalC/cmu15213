void transpose_submit(int M, int N, int A[N][M], int B[M][N])
{
  if (M == 32) {
    trans32(M, N, A, B);
    return;
  }
  else if (M == 64) {
    trans64(M, N, A, B);
    return;
  }
  else if (M == 61) {
    // trans61(M, N, A, B);
    return;
  }
}

